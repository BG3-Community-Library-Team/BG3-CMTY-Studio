/**
 * Apply a built-in theme's tokens to the document root via inline
 * style.setProperty(). For "custom" themes the CSS :root.custom block
 * handles variable resolution through --custom-* indirection — call
 * clearInlineThemeTokens() before switching to custom.
 */
export function applyThemeTokens(themeId: Exclude<ThemeId, "custom">): void {
  const tokens = THEME_TOKENS[themeId];
  if (!tokens) return;
  clearInlineThemeTokens();
  const root = document.documentElement;
  for (const [prop, value] of Object.entries(tokens)) {
    root.style.setProperty(prop, value);
  }
}

/**
 * Remove all inline --th-* style properties from the root element.
 * Called when switching to the "custom" theme so the CSS :root.custom
 * rules can take effect without being overridden by inline styles.
 */
export function clearInlineThemeTokens(): void {
  const root = document.documentElement;
  const toRemove: string[] = [];
  for (let i = 0; i < root.style.length; i++) {
    const prop = root.style[i];
    if (prop.startsWith("--th-")) toRemove.push(prop);
  }
  for (const prop of toRemove) root.style.removeProperty(prop);
}
