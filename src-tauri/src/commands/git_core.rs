#[tauri::command]
pub async fn cmd_git_status(mod_path: String) -> Result<Vec<GitFileStatus>, AppError> {
    blocking(move || {
        let repo = Repository::open(&mod_path)
            .map_err(|e| format!("Failed to open repository: {e}"))?;

        let mut opts = StatusOptions::new();
        opts.include_untracked(true)
            .recurse_untracked_dirs(true)
            .include_unmodified(false);

        let statuses = repo
            .statuses(Some(&mut opts))
            .map_err(|e| format!("Failed to get status: {e}"))?;

        let mut result = Vec::new();

        for entry in statuses.iter() {
            let path = entry.path().unwrap_or("").to_string();
            let status = entry.status();

            let has_index_change = status.intersects(
                Status::INDEX_NEW
                    | Status::INDEX_MODIFIED
                    | Status::INDEX_DELETED
                    | Status::INDEX_RENAMED,
            );

            let has_wt_change = status.intersects(
                Status::WT_NEW
                    | Status::WT_MODIFIED
                    | Status::WT_DELETED
                    | Status::WT_RENAMED
                    | Status::CONFLICTED,
            );

            // A file can appear twice: once staged and once unstaged
            if has_index_change {
                let old_path = entry
                    .head_to_index()
                    .and_then(|d| d.old_file().path().map(|p| p.to_string_lossy().into_owned()));
                result.push(GitFileStatus {
                    path: path.clone(),
                    status: status_string(status, true).to_string(),
                    staged: true,
                    old_path,
                });
            }

            if has_wt_change {
                let old_path = entry
                    .index_to_workdir()
                    .and_then(|d| d.old_file().path().map(|p| p.to_string_lossy().into_owned()));
                result.push(GitFileStatus {
                    path: path.clone(),
                    status: status_string(status, false).to_string(),
                    staged: false,
                    old_path,
                });
            }
        }

        Ok(result)
    })
    .await
}

#[tauri::command]
pub async fn cmd_git_stage(mod_path: String, paths: Vec<String>) -> Result<(), AppError> {
    blocking(move || {
        let repo = Repository::open(&mod_path)
            .map_err(|e| format!("Failed to open repository: {e}"))?;
        let mut index = repo.index().map_err(|e| format!("Failed to get index: {e}"))?;

        for p in &paths {
            index
                .add_path(Path::new(p))
                .map_err(|e| format!("Failed to stage '{p}': {e}"))?;
        }

        index.write().map_err(|e| format!("Failed to write index: {e}"))?;
        Ok(())
    })
    .await
}

