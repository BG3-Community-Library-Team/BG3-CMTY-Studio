/// Build a `GitRepoInfo` from an opened `Repository`.
fn repo_to_info(repo: &Repository) -> Result<GitRepoInfo, String> {
    let head_branch = repo
        .head()
        .ok()
        .and_then(|r| r.shorthand().map(String::from));

    let head_oid = repo.head().ok().and_then(|r| r.target()).map(|o| o.to_string());

    let is_detached = repo.head_detached().unwrap_or(false);

    let has_remotes = repo
        .remotes()
        .map(|r| !r.is_empty())
        .unwrap_or(false);

    let clean = repo
        .statuses(None)
        .map(|s| s.is_empty())
        .unwrap_or(true);

    let (ahead, behind) = compute_ahead_behind(repo);

    let shallow = repo.is_shallow();
    let bare = repo.is_bare();
    let submodules = repo
        .submodules()
        .map(|subs| subs.iter().map(|s| s.path().to_string_lossy().into_owned()).collect())
        .unwrap_or_default();

    Ok(GitRepoInfo {
        is_repo: true,
        head_branch,
        head_oid,
        is_detached,
        has_remotes,
        clean,
        ahead,
        behind,
        shallow,
        bare,
        submodules,
    })
}

/// Compute ahead/behind counts relative to the upstream tracking branch.
fn compute_ahead_behind(repo: &Repository) -> (u32, u32) {
    let head = match repo.head() {
        Ok(r) => r,
        Err(_) => return (0, 0),
    };
    let local_oid = match head.target() {
        Some(o) => o,
        None => return (0, 0),
    };
    let branch_name = match head.shorthand() {
        Some(n) => n.to_string(),
        None => return (0, 0),
    };
    let branch = match repo.find_branch(&branch_name, git2::BranchType::Local) {
        Ok(b) => b,
        Err(_) => return (0, 0),
    };
    let upstream = match branch.upstream() {
        Ok(u) => u,
        Err(_) => return (0, 0),
    };
    let upstream_oid = match upstream.get().target() {
        Some(o) => o,
        None => return (0, 0),
    };
    repo.graph_ahead_behind(local_oid, upstream_oid)
        .map(|(a, b)| (a as u32, b as u32))
        .unwrap_or((0, 0))
}

/// Map a `git2::Status` bitflag to a human-readable status string.
fn status_string(status: Status, staged: bool) -> &'static str {
    if staged {
        if status.intersects(Status::INDEX_NEW) {
            "added"
        } else if status.intersects(Status::INDEX_MODIFIED) {
            "modified"
        } else if status.intersects(Status::INDEX_DELETED) {
            "deleted"
        } else if status.intersects(Status::INDEX_RENAMED) {
            "renamed"
        } else {
            "modified"
        }
    } else if status.intersects(Status::WT_NEW) {
        "untracked"
    } else if status.intersects(Status::WT_MODIFIED) {
        "modified"
    } else if status.intersects(Status::WT_DELETED) {
        "deleted"
    } else if status.intersects(Status::WT_RENAMED) {
        "renamed"
    } else if status.intersects(Status::CONFLICTED) {
        "conflicted"
    } else {
        "modified"
    }
}

#[tauri::command]
pub async fn cmd_git_init(mod_path: String) -> Result<GitRepoInfo, AppError> {
    blocking(move || {
        let repo = Repository::init(&mod_path)
            .map_err(|e| format!("Failed to initialize git repo: {e}"))?;

        let gitignore_path = Path::new(&mod_path).join(".gitignore");
        std::fs::write(&gitignore_path, GITIGNORE_CONTENT)
            .map_err(|e| format!("Failed to write .gitignore: {e}"))?;

        repo_to_info(&repo)
    })
    .await
}

#[tauri::command]
pub async fn cmd_git_clone(url: String, target_path: String) -> Result<GitRepoInfo, AppError> {
    blocking(move || {
        let mut callbacks = git2::RemoteCallbacks::new();
        callbacks.credentials(|url, username, allowed| {
            crate::git::credentials::https_credentials_callback(url, username, allowed)
        });

        let mut fetch_opts = git2::FetchOptions::new();
        fetch_opts.remote_callbacks(callbacks);

        let repo = git2::build::RepoBuilder::new()
            .fetch_options(fetch_opts)
            .clone(&url, Path::new(&target_path))
            .map_err(|e| format!("Failed to clone repository: {e}"))?;

        repo_to_info(&repo)
    })
    .await
}

