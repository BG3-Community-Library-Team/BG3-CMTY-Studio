#[tauri::command]
pub async fn cmd_git_commit(
    mod_path: String,
    message: String,
) -> Result<GitCommitInfo, AppError> {
    blocking(move || {
        let repo = Repository::open(&mod_path)
            .map_err(|e| format!("Failed to open repository: {e}"))?;

        let mut index = repo.index().map_err(|e| format!("Failed to get index: {e}"))?;
        let tree_oid = index
            .write_tree()
            .map_err(|e| format!("Failed to write tree: {e}"))?;
        let tree = repo
            .find_tree(tree_oid)
            .map_err(|e| format!("Failed to find tree: {e}"))?;

        // Check if anything is staged (compare tree to HEAD tree)
        let head_tree = repo.head().ok().and_then(|r| r.peel_to_tree().ok());
        if let Some(ref ht) = head_tree {
            if ht.id() == tree_oid {
                return Err("Nothing to commit — stage changes first".to_string());
            }
        }

        let sig = crate::git::credentials::build_signature("", "")?;

        // Build parent list — empty for initial commit
        let parents: Vec<git2::Commit> = match repo.head() {
            Ok(head_ref) => {
                let commit = head_ref
                    .peel_to_commit()
                    .map_err(|e| format!("Failed to peel HEAD to commit: {e}"))?;
                vec![commit]
            }
            Err(_) => vec![],
        };
        let parent_refs: Vec<&git2::Commit> = parents.iter().collect();

        let commit_oid = repo
            .commit(Some("HEAD"), &sig, &sig, &message, &tree, &parent_refs)
            .map_err(|e| format!("Failed to create commit: {e}"))?;

        let commit = repo
            .find_commit(commit_oid)
            .map_err(|e| format!("Failed to find new commit: {e}"))?;

        Ok(commit_to_info(&commit))
    })
    .await
}

#[tauri::command]
pub async fn cmd_git_amend(
    mod_path: String,
    message: Option<String>,
) -> Result<GitCommitInfo, AppError> {
    blocking(move || {
        let repo = Repository::open(&mod_path)
            .map_err(|e| format!("Failed to open repository: {e}"))?;

        let head_ref = repo
            .head()
            .map_err(|e| format!("No HEAD commit to amend: {e}"))?;
        let head_commit = head_ref
            .peel_to_commit()
            .map_err(|e| format!("Failed to peel HEAD to commit: {e}"))?;

        let msg = message
            .as_deref()
            .unwrap_or_else(|| head_commit.message().unwrap_or(""));

        // Write index tree if there are staged changes, otherwise reuse HEAD tree
        let mut index = repo.index().map_err(|e| format!("Failed to get index: {e}"))?;
        let tree_oid = index
            .write_tree()
            .map_err(|e| format!("Failed to write tree: {e}"))?;
        let tree = repo
            .find_tree(tree_oid)
            .map_err(|e| format!("Failed to find tree: {e}"))?;

        let amended_oid = head_commit
            .amend(Some("HEAD"), None, None, None, Some(msg), Some(&tree))
            .map_err(|e| format!("Failed to amend commit: {e}"))?;

        let amended_commit = repo
            .find_commit(amended_oid)
            .map_err(|e| format!("Failed to find amended commit: {e}"))?;

        Ok(commit_to_info(&amended_commit))
    })
    .await
}

#[tauri::command]
pub async fn cmd_git_log(
    mod_path: String,
    limit: u32,
    offset: u32,
) -> Result<Vec<GitCommitInfo>, AppError> {
    blocking(move || {
        let repo = Repository::open(&mod_path)
            .map_err(|e| format!("Failed to open repository: {e}"))?;

        // Handle repos with no commits yet
        let head = match repo.head() {
            Ok(r) => r,
            Err(_) => return Ok(Vec::new()),
        };
        let head_oid = match head.target() {
            Some(o) => o,
            None => return Ok(Vec::new()),
        };

        let mut revwalk = repo
            .revwalk()
            .map_err(|e| format!("Failed to create revwalk: {e}"))?;
        revwalk
            .push(head_oid)
            .map_err(|e| format!("Failed to push HEAD to revwalk: {e}"))?;
        revwalk.set_sorting(Sort::TIME)
            .map_err(|e| format!("Failed to set sorting: {e}"))?;

        let mut result = Vec::new();
        for (i, oid_result) in revwalk.enumerate() {
            if i < offset as usize {
                continue;
            }
            if result.len() >= limit as usize {
                break;
            }
            let oid = oid_result.map_err(|e| format!("Revwalk error: {e}"))?;
            let commit = repo
                .find_commit(oid)
                .map_err(|e| format!("Failed to find commit {oid}: {e}"))?;
            result.push(commit_to_info(&commit));
        }

        Ok(result)
    })
    .await
}

#[tauri::command]
pub async fn cmd_git_show(
    mod_path: String,
    commit_oid: String,
) -> Result<GitCommitDetail, AppError> {
    blocking(move || {
        let repo = Repository::open(&mod_path)
            .map_err(|e| format!("Failed to open repository: {e}"))?;

        let oid = git2::Oid::from_str(&commit_oid)
            .map_err(|e| format!("Invalid OID '{commit_oid}': {e}"))?;
        let commit = repo
            .find_commit(oid)
            .map_err(|e| format!("Failed to find commit: {e}"))?;

        let commit_tree = commit
            .tree()
            .map_err(|e| format!("Failed to get commit tree: {e}"))?;

        // Get parent tree (or empty tree for initial commit)
        let parent_tree = if commit.parent_count() > 0 {
            Some(
                commit
                    .parent(0)
                    .map_err(|e| format!("Failed to get parent: {e}"))?
                    .tree()
                    .map_err(|e| format!("Failed to get parent tree: {e}"))?,
            )
        } else {
            None
        };

        let diff = repo
            .diff_tree_to_tree(parent_tree.as_ref(), Some(&commit_tree), None)
            .map_err(|e| format!("Failed to compute diff: {e}"))?;

        // Build file list from diff deltas
        let mut files = Vec::new();
        for delta in diff.deltas() {
            let path = delta
                .new_file()
                .path()
                .or_else(|| delta.old_file().path())
                .map(|p| p.to_string_lossy().to_string())
                .unwrap_or_default();

            let status = match delta.status() {
                git2::Delta::Added => "added",
                git2::Delta::Deleted => "deleted",
                git2::Delta::Modified => "modified",
                git2::Delta::Renamed => "renamed",
                git2::Delta::Copied => "copied",
                _ => "modified",
            };

            let old_path = if delta.status() == git2::Delta::Renamed {
                delta.old_file().path().map(|p| p.to_string_lossy().to_string())
            } else {
                None
            };

            files.push(GitFileStatus {
                path,
                status: status.to_string(),
                staged: true, // commit diffs are always "staged"
                old_path,
            });
        }

        // Build full diff with hunks using Patch API (avoids borrow checker issues with foreach)
        let mut diff_files: Vec<GitFileDiff> = Vec::new();

        for delta_idx in 0..diff.deltas().len() {
            let delta = diff.get_delta(delta_idx).expect("delta index in range");
            let file_path = delta
                .new_file()
                .path()
                .or_else(|| delta.old_file().path())
                .map(|p| p.to_string_lossy().to_string())
                .unwrap_or_default();

            let mut file_hunks = Vec::new();

            if let Ok(Some(patch)) = Patch::from_diff(&diff, delta_idx) {
                for h in 0..patch.num_hunks() {
                    let (hunk, _) = patch.hunk(h).map_err(|e| format!("Failed to get hunk: {e}"))?;
                    let mut lines = Vec::new();
                    for l in 0..patch.num_lines_in_hunk(h).map_err(|e| format!("Failed to get line count: {e}"))? {
                        let line = patch.line_in_hunk(h, l).map_err(|e| format!("Failed to get line: {e}"))?;
                        let origin = match line.origin() {
                            '+' => "+",
                            '-' => "-",
                            _ => " ",
                        };
                        lines.push(GitDiffLine {
                            origin: origin.to_string(),
                            content: String::from_utf8_lossy(line.content()).to_string(),
                            old_lineno: line.old_lineno(),
                            new_lineno: line.new_lineno(),
                        });
                    }
                    file_hunks.push(GitDiffHunk {
                        header: String::from_utf8_lossy(hunk.header()).trim_end().to_string(),
                        old_start: hunk.old_start(),
                        old_lines: hunk.old_lines(),
                        new_start: hunk.new_start(),
                        new_lines: hunk.new_lines(),
                        lines,
                    });
                }
            }

            diff_files.push(GitFileDiff {
                path: file_path,
                hunks: file_hunks,
                is_binary: delta.flags().is_binary(),
                old_size: Some(delta.old_file().size()),
                new_size: Some(delta.new_file().size()),
            });
        }

        Ok(GitCommitDetail {
            commit: commit_to_info(&commit),
            files,
            diff: diff_files,
        })
    })
    .await
}

