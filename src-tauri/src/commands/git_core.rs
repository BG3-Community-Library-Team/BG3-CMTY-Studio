#[tauri::command]
pub async fn cmd_git_unstage(mod_path: String, paths: Vec<String>) -> Result<(), AppError> {
    blocking(move || {
        let repo = Repository::open(&mod_path)
            .map_err(|e| format!("Failed to open repository: {e}"))?;

        // Try to get HEAD commit — may not exist on brand-new repos
        match repo.head() {
            Ok(head_ref) => {
                let head_commit = head_ref
                    .peel_to_commit()
                    .map_err(|e| format!("Failed to peel HEAD to commit: {e}"))?;
                let path_refs: Vec<&Path> = paths.iter().map(|p| Path::new(p.as_str())).collect();
                repo.reset_default(Some(head_commit.as_object()), &path_refs)
                    .map_err(|e| format!("Failed to unstage: {e}"))?;
            }
            Err(_) => {
                // No HEAD yet (empty repo) — remove paths from index directly
                let mut index = repo.index().map_err(|e| format!("Failed to get index: {e}"))?;
                for p in &paths {
                    index
                        .remove_path(Path::new(p))
                        .map_err(|e| format!("Failed to unstage '{p}': {e}"))?;
                }
                index.write().map_err(|e| format!("Failed to write index: {e}"))?;
            }
        }

        Ok(())
    })
    .await
}

#[tauri::command]
pub async fn cmd_git_discard(mod_path: String, paths: Vec<String>) -> Result<(), AppError> {
    blocking(move || {
        let repo = Repository::open(&mod_path)
            .map_err(|e| format!("Failed to open repository: {e}"))?;

        let mut opts = StatusOptions::new();
        opts.include_untracked(true).recurse_untracked_dirs(true);
        let statuses = repo
            .statuses(Some(&mut opts))
            .map_err(|e| format!("Failed to get status: {e}"))?;

        // Classify paths into tracked (checkout from HEAD) vs untracked (delete)
        let mut tracked: Vec<String> = Vec::new();
        let mut untracked: Vec<String> = Vec::new();

        for p in &paths {
            let mut is_untracked = false;
            for entry in statuses.iter() {
                if entry.path() == Some(p.as_str()) && entry.status().intersects(Status::WT_NEW) {
                    is_untracked = true;
                    break;
                }
            }
            if is_untracked {
                untracked.push(p.clone());
            } else {
                tracked.push(p.clone());
            }
        }

        // Restore tracked files from HEAD
        if !tracked.is_empty() {
            let mut cb = git2::build::CheckoutBuilder::new();
            cb.force();
            for p in &tracked {
                cb.path(p);
            }
            repo.checkout_head(Some(&mut cb))
                .map_err(|e| format!("Failed to discard tracked changes: {e}"))?;
        }

        // Delete untracked files
        let root = Path::new(&mod_path);
        for p in &untracked {
            let full_path = root.join(p);
            if full_path.is_file() {
                std::fs::remove_file(&full_path)
                    .map_err(|e| format!("Failed to remove '{p}': {e}"))?;
            } else if full_path.is_dir() {
                std::fs::remove_dir_all(&full_path)
                    .map_err(|e| format!("Failed to remove directory '{p}': {e}"))?;
            }
        }

        Ok(())
    })
    .await
}

#[tauri::command]
pub async fn cmd_git_diff_file(
    mod_path: String,
    path: String,
    staged: bool,
) -> Result<GitFileDiff, AppError> {
    blocking(move || {
        let repo = Repository::open(&mod_path)
            .map_err(|e| format!("Failed to open repository: {e}"))?;

        let mut diff_opts = DiffOptions::new();
        diff_opts.pathspec(&path);

        let diff = if staged {
            // Diff index (staged) against HEAD tree
            let head_tree = repo
                .head()
                .ok()
                .and_then(|r| r.peel_to_tree().ok());
            repo.diff_tree_to_index(head_tree.as_ref(), None, Some(&mut diff_opts))
                .map_err(|e| format!("Failed to compute staged diff: {e}"))?
        } else {
            // Diff working dir against index
            repo.diff_index_to_workdir(None, Some(&mut diff_opts))
                .map_err(|e| format!("Failed to compute working dir diff: {e}"))?
        };

        let mut hunks: Vec<GitDiffHunk> = Vec::new();
        let mut is_binary = false;
        let mut old_size: Option<u64> = None;
        let mut new_size: Option<u64> = None;

        // Use Patch API to avoid borrow checker issues with diff.foreach
        for delta_idx in 0..diff.deltas().len() {
            let delta = diff.get_delta(delta_idx).expect("delta index in range");
            if delta.flags().is_binary() {
                is_binary = true;
            }
            old_size = Some(delta.old_file().size());
            new_size = Some(delta.new_file().size());

            if let Ok(Some(patch)) = Patch::from_diff(&diff, delta_idx) {
                for h in 0..patch.num_hunks() {
                    let (hunk, _num_lines) = patch.hunk(h).map_err(|e| format!("Failed to get hunk: {e}"))?;
                    let mut lines = Vec::new();
                    for l in 0..patch.num_lines_in_hunk(h).map_err(|e| format!("Failed to get line count: {e}"))? {
                        let line = patch.line_in_hunk(h, l).map_err(|e| format!("Failed to get line: {e}"))?;
                        let origin = match line.origin() {
                            '+' => "+",
                            '-' => "-",
                            _ => " ",
                        };
                        lines.push(GitDiffLine {
                            origin: origin.to_string(),
                            content: String::from_utf8_lossy(line.content()).to_string(),
                            old_lineno: line.old_lineno(),
                            new_lineno: line.new_lineno(),
                        });
                    }
                    hunks.push(GitDiffHunk {
                        header: String::from_utf8_lossy(hunk.header()).trim_end().to_string(),
                        old_start: hunk.old_start(),
                        old_lines: hunk.old_lines(),
                        new_start: hunk.new_start(),
                        new_lines: hunk.new_lines(),
                        lines,
                    });
                }
            }
        }

        Ok(GitFileDiff {
            path,
            hunks,
            is_binary,
            old_size,
            new_size,
        })
    })
    .await
}

